package org.gs4tr.termmanager.model.dto;

public class Organization {

    private Integer _availableTasks;

    private OrganizationInfo _organizationInfo;

    private Organization _parentOrganization;

    private Task[] _tasks;

    private String _ticket;

    public Integer getAvailableTasks() {
	return _availableTasks;
    }

    public OrganizationInfo getOrganizationInfo() {
	return _organizationInfo;
    }

    public Organization getParentOrganization() {
	return _parentOrganization;
    }

    public Task[] getTasks() {
	return _tasks;
    }

    public String getTicket() {
	return _ticket;
    }

    public void setAvailableTasks(Integer availableTasks) {
	_availableTasks = availableTasks;
    }

    public void setOrganizationInfo(OrganizationInfo organizationInfo) {
	_organizationInfo = organizationInfo;
    }

    public void setParentOrganization(Organization parentOrganization) {
	_parentOrganization = parentOrganization;
    }

    public void setTasks(Task[] tasks) {
	_tasks = tasks;
    }

    public void setTicket(String ticket) {
	_ticket = ticket;
    }

}
