package org.gs4tr.termmanager.model.dto;

import java.util.ArrayList;
import java.util.List;

public class TermPreviewModel {

    private Boolean _blacklisted;

    private List<DescriptionPreviewModel> _descriptionPreviewModels = new ArrayList<DescriptionPreviewModel>();

    private boolean _inTranslation = false;

    private String _markerId;

    private String _name;

    private List<DescriptionPreviewModel> _notePreviewModels = new ArrayList<DescriptionPreviewModel>();

    private boolean _sourceInTranslation = false;

    private String _termStatus;

    private String _ticket;

    public Boolean getBlacklisted() {
	return _blacklisted;
    }

    public List<DescriptionPreviewModel> getDescriptionPreviewModels() {
	return _descriptionPreviewModels;
    }

    public String getMarkerId() {
	return _markerId;
    }

    public String getName() {
	return _name;
    }

    public List<DescriptionPreviewModel> getNotePreviewModels() {
	return _notePreviewModels;
    }

    public String getTermStatus() {
	return _termStatus;
    }

    public String getTicket() {
	return _ticket;
    }

    public boolean isInTranslation() {
	return _inTranslation;
    }

    public boolean isSourceInTranslation() {
	return _sourceInTranslation;
    }

    public void setBlacklisted(Boolean blacklisted) {
	_blacklisted = blacklisted;
    }

    public void setDescriptionPreviewModels(List<DescriptionPreviewModel> descriptionPreviewModels) {
	_descriptionPreviewModels = descriptionPreviewModels;
    }

    public void setInTranslation(boolean inTranslation) {
	_inTranslation = inTranslation;
    }

    public void setMarkerId(String markerId) {
	_markerId = markerId;
    }

    public void setName(String name) {
	_name = name;
    }

    public void setNotePreviewModels(List<DescriptionPreviewModel> notePreviewModels) {
	_notePreviewModels = notePreviewModels;
    }

    public void setSourceInTranslation(boolean sourceInTranslation) {
	_sourceInTranslation = sourceInTranslation;
    }

    public void setTermStatus(String termStatus) {
	_termStatus = termStatus;
    }

    public void setTicket(String ticket) {
	_ticket = ticket;
    }
}
