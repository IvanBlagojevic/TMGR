package org.gs4tr.termmanager.model.dto;

import java.util.List;
import java.util.Map;

public class TermModel {

    private String _termEntryTicket;
    private Language _language;
    private Map<String, ChildModel> _source;
    private Map<String, ChildModel> _target;
    private Map<String, List<Language>> _tooltip;
    private List<Description> _termEntryAttributes;
    private List<TermEntryResourceTrack> _multimediaTickets;
    private String _targetLanguageMarkerId;
    private String _projectTicket;

    private String _termEntryMarkerId;

    public Language getLanguage() {
	return _language;
    }

    public List<TermEntryResourceTrack> getMultimediaTickets() {
	return _multimediaTickets;
    }

    public String getProjectTicket() {
	return _projectTicket;
    }

    public Map<String, ChildModel> getSource() {
	return _source;
    }

    public Map<String, ChildModel> getTarget() {
	return _target;
    }

    public String getTargetLanguageMarkerId() {
	return _targetLanguageMarkerId;
    }

    public List<Description> getTermEntryAttributes() {
	return _termEntryAttributes;
    }

    public String getTermEntryMarkerId() {
	return _termEntryMarkerId;
    }

    public String getTermEntryTicket() {
	return _termEntryTicket;
    }

    public Map<String, List<Language>> getTooltip() {
	return _tooltip;
    }

    public void setLanguage(Language language) {
	_language = language;
    }

    public void setMultimediaTickets(List<TermEntryResourceTrack> multimediaTickets) {
	_multimediaTickets = multimediaTickets;
    }

    public void setProjectTicket(String projectTicket) {
	_projectTicket = projectTicket;
    }

    public void setSource(Map<String, ChildModel> source) {
	_source = source;
    }

    public void setTarget(Map<String, ChildModel> target) {
	_target = target;
    }

    public void setTargetLanguageMarkerId(String targetLanguageMarkerId) {
	_targetLanguageMarkerId = targetLanguageMarkerId;
    }

    public void setTermEntryAttributes(List<Description> termEntryAttributes) {
	_termEntryAttributes = termEntryAttributes;
    }

    public void setTermEntryMarkerId(String termEntryMarkerId) {
	_termEntryMarkerId = termEntryMarkerId;
    }

    public void setTermEntryTicket(String termEntryTicket) {
	_termEntryTicket = termEntryTicket;
    }

    public void setTooltip(Map<String, List<Language>> tooltip) {
	_tooltip = tooltip;
    }

}
