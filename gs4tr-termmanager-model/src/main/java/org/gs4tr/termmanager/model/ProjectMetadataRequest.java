package org.gs4tr.termmanager.model;

import java.util.List;

public class ProjectMetadataRequest {

    private List<String> _languages;

    private String _organizationName;

    private String _projectName;

    private String _projectShortcode;

    private String _username;

    public ProjectMetadataRequest(List<String> languages, String organizationName, String projectName,
	    String projectShortcode, String username) {

	_languages = languages;
	_organizationName = organizationName;
	_projectName = projectName;
	_projectShortcode = projectShortcode;
	_username = username;
    }

    public ProjectMetadataRequest() {
    }

    public List<String> getLanguages() {
	return _languages;
    }

    public String getOrganizationName() {
	return _organizationName;
    }

    public String getProjectName() {
	return _projectName;
    }

    public String getProjectShortcode() {
	return _projectShortcode;
    }

    public String getUsername() {
	return _username;
    }

    public void setLanguages(List<String> languages) {
	_languages = languages;
    }

    public void setOrganizationName(String organizationName) {
	_organizationName = organizationName;
    }

    public void setProjectName(String projectName) {
	_projectName = projectName;
    }

    public void setProjectShortcode(String projectShortcode) {
	_projectShortcode = projectShortcode;
    }

    public void setUsername(String username) {
	_username = username;
    }
}
