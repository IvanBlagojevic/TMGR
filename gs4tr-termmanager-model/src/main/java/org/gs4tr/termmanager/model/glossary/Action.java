package org.gs4tr.termmanager.model.glossary;

/**
 * A set of values that represent the possible action types.
 *
 * @see TermEntry
 */
public enum Action {
    /**
     * Indicates that {@code TermEntry} was created in the UI.
     */
    ADDED,

    /**
     * Indicates that {@code TermEntry} has been added remotely.
     */
    ADDED_REMOTELY,

    /**
     * Indicates that {@code TermEntry} has been updated after user approved
     * translations.
     */
    APPROVED_TRANSLATIONS,

    /**
     * Indicates that {@code TermEntry} has been updated after user cancel
     * {@code Submission}.
     */
    CANCELED_TRANSLATIONS,

    /**
     * Indicates that {@code TermEntry} has been updated in the UI (translation
     * added/edited/removed, attribute/note added/edited/removed, status
     * edited).
     */
    EDITED,

    /**
     * Indicates that {@code TermEntry} has been updated remotely.
     */
    EDITED_REMOTELY,

    /**
     * Indicates that {@code TermEntry} has been updated via import.
     */
    IMPORTED,

    /**
     * Indicates that {@code TermEntry} was updated due to a UI merge.
     */
    MERGED,

    /**
     * Indicates that action type is not available for {@code TermEntry}.
     */
    NOT_AVAILABLE,

    /**
     * Indicates that {@code TermEntry} has been updated after user resubmit
     * translations.
     */
    RESUBMITTED,

    /**
     * Indicates that {@code TermEntry} has been sent to translation.
     */
    SENT_TO_TRANSLATION,

    /**
     * Indicates that {@code TermEntry} has been cloned.
     */
    CLONED,

    /**
     * Indicates that {@code TermEntry} has been updated after user update
     * translations.
     */
    UPDATED_TRANSLATIONS;
}
