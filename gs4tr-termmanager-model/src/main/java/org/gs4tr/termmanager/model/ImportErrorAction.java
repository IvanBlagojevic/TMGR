package org.gs4tr.termmanager.model;

public enum ImportErrorAction {

    IMPORT_TRANSLATIONS(0, "Import translations"), SKIP(1, "Skip");

    private int _identifier;

    private String _name;

    private ImportErrorAction(int identifier, String value) {
	_identifier = identifier;
	_name = value;
    }

    public int getIdentifier() {
	return _identifier;
    }

    public String getName() {
	return _name;
    }
}
