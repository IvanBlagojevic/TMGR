package org.gs4tr.termmanager.model.dto;

import java.util.List;

public class UserLanguageModel {
    private String _userName;
    private String _userTicket;
    private String _role;
    private List<ProjectUserLanguageModel> _allLanguages;

    public List<ProjectUserLanguageModel> getAllLanguages() {
	return _allLanguages;
    }

    public String getRole() {
	return _role;
    }

    public String getUserName() {
	return _userName;
    }

    public String getUserTicket() {
	return _userTicket;
    }

    public void setAllLanguages(List<ProjectUserLanguageModel> allLanguages) {
	_allLanguages = allLanguages;
    }

    public void setRole(String role) {
	_role = role;
    }

    public void setUserName(String userName) {
	_userName = userName;
    }

    public void setUserTicket(String userTicket) {
	_userTicket = userTicket;
    }

}
