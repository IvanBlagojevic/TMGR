package org.gs4tr.termmanager.model.dto;

import org.gs4tr.termmanager.model.TermTooltip;

public class TargetTerm {
    private String _languageId;

    private Long _submissionId;

    private String _submissionName;

    private String _targetAlignment;

    private TargetDtoTermTranslation[] _targetSynonyms;

    private TargetDtoTermTranslation _targetTerm;

    private TermTooltip[] _targetTermTooltip;

    public String getLanguageId() {
	return _languageId;
    }

    public Long getSubmissionId() {
	return _submissionId;
    }

    public String getSubmissionName() {
	return _submissionName;
    }

    public String getTargetAlignment() {
	return _targetAlignment;
    }

    public TargetDtoTermTranslation[] getTargetSynonyms() {
	return _targetSynonyms;
    }

    public TargetDtoTermTranslation getTargetTerm() {
	return _targetTerm;
    }

    public TermTooltip[] getTargetTermTooltip() {
	return _targetTermTooltip;
    }

    public void setLanguageId(String languageId) {
	_languageId = languageId;
    }

    public void setSubmissionId(Long submissionId) {
	_submissionId = submissionId;
    }

    public void setSubmissionName(String submissionName) {
	_submissionName = submissionName;
    }

    public void setTargetAlignment(String targetAlignment) {
	_targetAlignment = targetAlignment;
    }

    public void setTargetSynonyms(TargetDtoTermTranslation[] targetSynonyms) {
	_targetSynonyms = targetSynonyms;
    }

    public void setTargetTerm(TargetDtoTermTranslation targetTerm) {
	_targetTerm = targetTerm;
    }

    public void setTargetTermTooltip(TermTooltip[] targetTermTooltip) {
	_targetTermTooltip = targetTermTooltip;
    }

}
