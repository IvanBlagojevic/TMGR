package org.gs4tr.termmanager.service.model.command;

import java.util.List;

public class BaseLingualCommand {

    private String _sourceId;

    private String _sourceLocale;

    private List<TranslationViewCommand> _targetTermsList;

    private String _termEntryId;

    public String getSourceId() {
	return _sourceId;
    }

    public String getSourceLocale() {
	return _sourceLocale;
    }

    public List<TranslationViewCommand> getTargetTerms() {
	return _targetTermsList;
    }

    public String getTermEntryId() {
	return _termEntryId;
    }

    public void setSourceId(String sourceId) {
	_sourceId = sourceId;
    }

    public void setSourceLocale(String sourceLocale) {
	_sourceLocale = sourceLocale;
    }

    public void setTargetTerms(List<TranslationViewCommand> targetTerms) {
	_targetTermsList = targetTerms;
    }

    public void setTermEntryId(String termEntryId) {
	_termEntryId = termEntryId;
    }
}
