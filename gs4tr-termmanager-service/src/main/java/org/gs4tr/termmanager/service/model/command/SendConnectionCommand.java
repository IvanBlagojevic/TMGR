package org.gs4tr.termmanager.service.model.command;

import java.util.Set;

public class SendConnectionCommand {

    private Set<String> _connectionStrings;

    private String _emails;

    private String _sourceLanguage;

    public Set<String> getConnectionStrings() {
	return _connectionStrings;
    }

    public String getEmails() {
	return _emails;
    }

    public String getSourceLanguage() {
	return _sourceLanguage;
    }

    public void setConnectionStrings(Set<String> connectionStrings) {
	_connectionStrings = connectionStrings;
    }

    public void setEmails(String emails) {
	_emails = emails;
    }

    public void setSourceLanguage(String sourceLanguage) {
	_sourceLanguage = sourceLanguage;
    }
}
