package org.gs4tr.termmanager.service.model.command;

import java.util.List;

import org.gs4tr.foundation.modules.entities.model.UserInfo;

public class UserLanguageCommand {

    private boolean _genericUser = false;

    private String _roleId;

    private Long _userId;

    private UserInfo _userInfo;

    private List<String> _userLanguages;

    public String getRoleId() {
	return _roleId;
    }

    public Long getUserId() {
	return _userId;
    }

    public UserInfo getUserInfo() {
	return _userInfo;
    }

    public List<String> getUserLanguages() {
	return _userLanguages;
    }

    public boolean isGenericUser() {
	return _genericUser;
    }

    public void setGenericUser(boolean genericUser) {
	_genericUser = genericUser;
    }

    public void setRoleId(String roleId) {
	_roleId = roleId;
    }

    public void setUserId(Long userId) {
	_userId = userId;
    }

    public void setUserInfo(UserInfo userInfo) {
	_userInfo = userInfo;
    }

    public void setUserLanguages(List<String> userLanguages) {
	_userLanguages = userLanguages;
    }

}
