package org.gs4tr.termmanager.service.model.command;

import java.util.List;

public class AssignUserLanguageCommand {

    private Long _userId;

    private List<String> _userLanguages;

    public Long getUserId() {
	return _userId;
    }

    public List<String> getUserLanguages() {
	return _userLanguages;
    }

    public void setUserId(Long userId) {
	_userId = userId;
    }

    public void setUserLanguages(List<String> userLanguages) {
	_userLanguages = userLanguages;
    }

}
