package org.gs4tr.termmanager.service.model.command;

import java.util.List;

public class AssignUserProjectRoleCommand {

    private Long _projectId;

    private List<UserRoleCommand> _userRoles;

    public Long getProjectId() {
	return _projectId;
    }

    public List<UserRoleCommand> getUserRoles() {
	return _userRoles;
    }

    public void setProjectId(Long projectId) {
	_projectId = projectId;
    }

    public void setUserRoles(List<UserRoleCommand> userRoles) {
	_userRoles = userRoles;
    }

}
