package org.gs4tr.termmanager.service.model.command;

import java.util.List;

import org.gs4tr.termmanager.model.Attribute;
import org.gs4tr.termmanager.model.AttributeLevelEnum;
import org.gs4tr.termmanager.model.TermEntryAttributeTypeEnum;

public class AssignProjectAttributesCommand {
    private List<Attribute> _projectAttributes;

    private Long _projectId;

    private TermEntryAttributeTypeEnum _termEntryAttributeType;

    private AttributeLevelEnum _attributeLevelEnum;

    public AttributeLevelEnum getAttributeLevelEnum() {
	return _attributeLevelEnum;
    }

    public List<Attribute> getProjectAttributes() {
	return _projectAttributes;
    }

    public Long getProjectId() {
	return _projectId;
    }

    public TermEntryAttributeTypeEnum getTermEntryAttributeType() {
	return _termEntryAttributeType;
    }

    public void setAttributeLevelEnum(AttributeLevelEnum attributeLevelEnum) {
	_attributeLevelEnum = attributeLevelEnum;
    }

    public void setProjectAttributes(List<Attribute> projectAttributes) {
	_projectAttributes = projectAttributes;
    }

    public void setProjectId(Long projectId) {
	_projectId = projectId;
    }

    public void setTermEntryAttributeType(TermEntryAttributeTypeEnum termEntryAttributeType) {
	_termEntryAttributeType = termEntryAttributeType;
    }

}
