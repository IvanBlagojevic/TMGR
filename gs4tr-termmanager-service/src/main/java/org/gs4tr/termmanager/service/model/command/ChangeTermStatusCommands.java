package org.gs4tr.termmanager.service.model.command;

import java.util.List;

public class ChangeTermStatusCommands extends BaseTermActionCommand {

    private List<TermCommandPerProject> _changeStatusCommands;

    private String _termStatus;

    public List<TermCommandPerProject> getChangeStatusCommands() {
	return _changeStatusCommands;
    }

    public String getTermStatus() {
	return _termStatus;
    }

    public void setChangeStatusCommands(List<TermCommandPerProject> changeStatusCommands) {
	_changeStatusCommands = changeStatusCommands;
    }

    public void setTermStatus(String termStatus) {
	_termStatus = termStatus;
    }
}
