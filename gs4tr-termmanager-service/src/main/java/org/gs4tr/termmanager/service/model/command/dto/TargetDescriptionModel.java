package org.gs4tr.termmanager.service.model.command.dto;

import org.gs4tr.termmanager.model.dto.ExtendedDescription;

public class TargetDescriptionModel {
    private ExtendedDescription[] _targetDescriptions;

    private boolean _targetIsRTL;

    private String _targetLanguage;

    private ExtendedDescription[] _targetNotes;

    public ExtendedDescription[] getTargetDescriptions() {
	return _targetDescriptions;
    }

    public boolean getTargetIsRTL() {
	return _targetIsRTL;
    }

    public String getTargetLanguage() {
	return _targetLanguage;
    }

    public ExtendedDescription[] getTargetNotes() {
	return _targetNotes;
    }

    public void setTargetDescriptions(ExtendedDescription[] targetDescriptions) {
	_targetDescriptions = targetDescriptions;
    }

    public void setTargetIsRTL(boolean targetIsRTL) {
	_targetIsRTL = targetIsRTL;
    }

    public void setTargetLanguage(String targetLanguage) {
	_targetLanguage = targetLanguage;
    }

    public void setTargetNotes(ExtendedDescription[] targetNotes) {
	_targetNotes = targetNotes;
    }

}
