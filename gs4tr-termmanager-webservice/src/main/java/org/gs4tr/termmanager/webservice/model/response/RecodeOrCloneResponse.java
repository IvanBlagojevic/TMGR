package org.gs4tr.termmanager.webservice.model.response;

public class RecodeOrCloneResponse extends BaseResponse {

    private String _message;

    // Default constructor for objectMapper
    public RecodeOrCloneResponse() {

    }

    public RecodeOrCloneResponse(int code, boolean success, String message) {
	setReturnCode(code);
	setSuccess(success);
	setMessage(message);
    }

    public String getMessage() {
	return _message;
    }

    public void setMessage(String message) {
	_message = message;
    }
}
