package org.gs4tr.termmanager.webservice.model.response;

import java.util.List;

import org.gs4tr.termmanager.model.ProjectMetadata;

public class ProjectMetadataResponse extends BaseResponse {

    private List<ProjectMetadata> _projectMetadata;

    public ProjectMetadataResponse(List<ProjectMetadata> projectMetadata) {
	_projectMetadata = projectMetadata;
    }

    public ProjectMetadataResponse() {
    }

    public List<ProjectMetadata> getProjectMetadata() {
	return _projectMetadata;
    }

    public void setProjectMetadata(List<ProjectMetadata> projectMetadata) {
	_projectMetadata = projectMetadata;
    }
}
