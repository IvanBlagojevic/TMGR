package org.gs4tr.termmanager.webmvc.model.search;

import java.util.List;

public class LanguageSearchBox extends SearchBox {

    private List<LanguageSearch> _items;

    private boolean _required = true;

    public LanguageSearchBox() {
    }

    public List<LanguageSearch> getItems() {
	return _items;
    }

    public boolean isRequired() {
	return _required;
    }

    public void setItems(List<LanguageSearch> items) {
	_items = items;
    }

    public void setRequired(boolean required) {
	_required = required;
    }
}
