package org.gs4tr.termmanager.webmvc.model.commands;

import org.gs4tr.foundation.modules.entities.model.types.EntityType;
import org.gs4tr.termmanager.model.search.ItemFolderEnum;
import org.gs4tr.termmanager.service.model.command.JsonCommand;

public class FolderViewUpdateCommand extends JsonCommand {

    private ItemFolderEnum _folder;

    private String _metadata;

    private EntityType _view;

    public ItemFolderEnum getFolder() {
	return _folder;
    }

    public String getMetadata() {
	return _metadata;
    }

    public EntityType getView() {
	return _view;
    }

    public void setFolder(ItemFolderEnum folder) {
	_folder = folder;
    }

    public void setMetadata(String metadata) {
	_metadata = metadata;
    }

    public void setView(EntityType view) {
	_view = view;
    }
}
