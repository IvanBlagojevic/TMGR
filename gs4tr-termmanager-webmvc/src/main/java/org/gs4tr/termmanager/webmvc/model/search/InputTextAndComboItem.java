package org.gs4tr.termmanager.webmvc.model.search;

public class InputTextAndComboItem {

    private String _value1;

    private String _value2;

    public InputTextAndComboItem() {
    }

    public InputTextAndComboItem(String value1, String value2) {
	_value1 = value1;
	_value2 = value2;
    }

    /** input text value */
    public String getValue1() {
	return _value1;
    }

    /** combo box value */
    public String getValue2() {
	return _value2;
    }

    public void setValue1(String value1) {
	_value1 = value1;
    }

    public void setValue2(String value2) {
	_value2 = value2;
    }
}
