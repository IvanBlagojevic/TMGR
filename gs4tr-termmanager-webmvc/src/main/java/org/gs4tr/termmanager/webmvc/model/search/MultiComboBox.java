package org.gs4tr.termmanager.webmvc.model.search;

import java.util.List;

public class MultiComboBox extends Control {

    private boolean _required = false;

    private List<String> _value;

    private List<SimpleComboItem> _values;

    public MultiComboBox(SearchCriteria searchCriteria) {
	super(searchCriteria);
    }

    public List<String> getValue() {
	return _value;
    }

    public List<SimpleComboItem> getValues() {
	return _values;
    }

    public boolean isRequired() {
	return _required;
    }

    public void setRequired(boolean required) {
	_required = required;
    }

    public void setValue(List<String> value) {
	_value = value;
    }

    public void setValues(List<SimpleComboItem> values) {
	_values = values;
    }
}
