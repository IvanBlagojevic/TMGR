package org.gs4tr.termmanager.webmvc.model.commands;

import org.gs4tr.termmanager.service.model.command.JsonCommand;

public class CustomFilterCommand extends JsonCommand {
    private String _customFolder;

    private Boolean _insert;

    private String _jsonSearch;

    private String _originalFolder;

    private String _url;

    public String getCustomFolder() {
	return _customFolder;
    }

    public Boolean getInsert() {
	return _insert;
    }

    public String getJsonSearch() {
	return _jsonSearch;
    }

    public String getOriginalFolder() {
	return _originalFolder;
    }

    public String getUrl() {
	return _url;
    }

    public void setCustomFolder(String customFolder) {
	_customFolder = customFolder;
    }

    public void setInsert(Boolean insert) {
	_insert = insert;
    }

    public void setJsonSearch(String jsonSearch) {
	_jsonSearch = jsonSearch;
    }

    public void setOriginalFolder(String originalFolder) {
	_originalFolder = originalFolder;
    }

    public void setUrl(String url) {
	_url = url;
    }

}
