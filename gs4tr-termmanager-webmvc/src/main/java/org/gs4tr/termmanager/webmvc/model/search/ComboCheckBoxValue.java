package org.gs4tr.termmanager.webmvc.model.search;

public class ComboCheckBoxValue {

    private SearchCheckBox[] _checkBoxValues;

    private String _comboBoxValue;

    private SearchRadioBox[] _radioBoxValues;

    public SearchCheckBox[] getCheckBoxValues() {
	return _checkBoxValues;
    }

    public String getComboBoxValue() {
	return _comboBoxValue;
    }

    public SearchRadioBox[] getRadioBoxValues() {
	return _radioBoxValues;
    }

    public void setCheckBoxValues(SearchCheckBox[] checkBoxValues) {
	_checkBoxValues = checkBoxValues;
    }

    public void setComboBoxValue(String comboBoxValue) {
	_comboBoxValue = comboBoxValue;
    }

    public void setRadioBoxValues(SearchRadioBox[] radioBoxValues) {
	_radioBoxValues = radioBoxValues;
    }
}
